var nodemailer = require('nodemailer');
var app = require('express')();

if (app.get('env') === 'development') {
    var gmailCreds = require("./gmailCreds.json");

    var transporter = nodemailer.createTransport({
        service: 'gmail',
        auth: {
            user: gmailCreds.user,
            pass: gmailCreds.pass
        }
    });
} else {
    var transporter = nodemailer.createTransport({
        service: 'gmail',
        auth: {
            user: process.env.GMAILACCOUNT,
            pass: process.env.GMAILPASS
        }
    });
}

function constructEmail(emailData) {
    var mailOptions = {
        from: 'Galactic Shockball League <do-not-reply@mail.com>',
        to: emailData.emailAddress,
        subject: emailData.emailSubject,
        html: parseHtml(emailData.userFirstName, emailData.userLastName)
    };
    sendEmail(mailOptions);
};

function sendEmail(mailOptions) {
    transporter.sendMail(mailOptions, function(error, info) {
        if(error) {
            return console.log(error);
        }
        console.log('Message sent to ' + info.accepted);
    });
}

